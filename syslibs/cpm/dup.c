#include <cpm.h>
#include <string.h>

int dup(uint8_t fd)
{
	register struct fcb *	fp;

	if(_fcb[fd].use && (fp = getfcb())) {
// 		*fp = _fcb[fd];
		memcpy(fp, &_fcb[fd], sizeof(struct fcb));
		return fp - _fcb;
	}
	return -1;
}
