#include <stdarg.h>
#include <cpm.h>

extern int _doprnt(const char *, va_list, FILE *);

int cpm_printf(const char *format, ...)
{
	va_list ap;
	int retval;

	va_start(ap, format);

	retval = _doprnt(format, ap, stdout);

	va_end(ap);

	return retval;
}


