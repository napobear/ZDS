#include <stdarg.h>
#include <cpm.h>

extern int _doprnt(const char *, va_list, FILE *);

int fprintf(FILE * file, const char * f, ...)
{
	va_list arg;
	int i;

	va_start (arg, f);
	i = _doprnt (f, arg, file);
	va_end (arg);

	return i;
}
