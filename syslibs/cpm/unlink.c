#include <cpm.h>

int unlink(char * name)
{
	struct fcb	fc;
	uint8_t		luid;
	short		retval;

	if(setfcb(&fc, name))
		return 0;
	luid = getuid();
	setuid(fc.uid);
	retval = bdos(CPMDEL, (uint16_t)&fc);
	setuid(luid);
	return retval;
}
