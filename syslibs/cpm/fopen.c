#include <cpm.h>

/*
 *	fopen.c - stdio fopen
 */

FILE * fopen(char * name, char * mode)
{
	register FILE *	f;

	for(f = _iob ; f <= &_iob[_NFILE-1] ; f++)
		if(!(f->_flag & (_IOREAD|_IOWRT)))
			break;
	if(f > &_iob[_NFILE-1])
		return((FILE *)NULL);
	return freopen(name, mode, f);
}
