#include <cpm.h>


/*
 *	putw for Zios
 */

int putw(int w, register FILE * stream)
{
	if(putc(w&0xFF, stream) == EOF || putc((w >> 8)&0xFF, stream) == EOF)
		return(EOF);
	return(w);
}
