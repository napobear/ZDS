#include <cpm.h>

/*
 *	puts and fputs for Zios stdio
 */

void fputs(char * s, register FILE * f)
{
	while(*s)
		putc(*s++, f);

}

void puts(char * s)
{
	fputs(s, stdout);
	putc('\r', stdout);
	putc('\n', stdout);
}
