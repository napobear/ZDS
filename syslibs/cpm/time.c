#include <cpm.h>


#define	T_GET	0x69

time_t time(time_t * tp)
{
	struct tod	tod;
	time_t		t;

	tod.hour = tod.min = tod.sec = tod.day = 0;
	tod.sec = (char)bdos(T_GET, (uint16_t)&tod);
	t = convtime(&tod);
	if(tp)
		*tp = t;
	return t;
}
